package com.example.oops.q05;

public abstract class CalcAbs {

	 void sum(int a, int b) {
		 System.out.println("sum of CalcAbs "+(a+b));
	 }
	 void sub(int a, int b) {
		 System.out.println("sub of CalcAbs "+(a-b));
	 }
	 void mul(int a, int b) {
		 System.out.println("mul of CalcAbs "+(a*b));
	 }
	 void div(int a,int b) {
		 System.out.println("div of CalcAbs "+(a/b));
	 }
}
